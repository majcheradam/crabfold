"use client";

import { Button } from "@crabfold/ui/components/button";
import {
  Card,
  CardContent,
  CardHeader,
  CardTitle,
  CardDescription,
} from "@crabfold/ui/components/card";
import {
  Copy,
  Check,
  Download,
  GitBranch,
  Rocket,
  RotateCcw,
  FileText,
  FolderTree,
  Settings,
  Brain,
} from "lucide-react";
import { useState } from "react";

const MOCK_FILES = [
  { icon: Settings, lines: 47, name: "config.yaml" },
  { icon: FileText, lines: 12, name: "WORKSPACE.md" },
  { icon: FolderTree, lines: null, name: "skills/" },
  { icon: Brain, lines: 34, name: "skills/github-triage/SKILL.md" },
  { icon: FileText, lines: 89, name: "skills/github-triage/handler.ts" },
  { icon: FolderTree, lines: null, name: "memory/" },
  { icon: FileText, lines: 8, name: "memory/context.md" },
];

const MOCK_CONFIG = `# Generated by crabfold
name: github-issue-triager
framework: openclaw
version: 1.0.0

gateway:
  channels:
    - type: github
      repo: "your-org/your-repo"
      events: [issues.opened, issues.edited]

brain:
  model: claude-sonnet-4-6
  strategy: react
  max_iterations: 5

skills:
  - name: github-triage
    description: "Label and assign incoming GitHub issues"
    triggers:
      - event: issues.opened
    actions:
      - classify_issue
      - apply_labels
      - assign_owner

heartbeat:
  schedule: "*/5 * * * *"
  tasks:
    - check_unassigned_issues

memory:
  backend: markdown
  path: ./memory`;

export function AgentResult({
  prompt,
  onReset,
}: {
  prompt: string;
  onReset: () => void;
}) {
  const [copied, setCopied] = useState(false);
  const [activeTab, setActiveTab] = useState<"files" | "config">("config");

  const handleCopy = () => {
    navigator.clipboard.writeText(MOCK_CONFIG);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  return (
    <div className="flex min-h-svh flex-col items-center justify-center px-4 py-12">
      <div className="flex w-full max-w-3xl flex-col gap-6">
        {/* Header */}
        <div className="flex items-start justify-between">
          <div className="flex flex-col gap-1">
            <h2 className="text-lg font-semibold text-foreground">
              Your agent is ready
            </h2>
            <p className="text-xs text-muted-foreground">{prompt}</p>
          </div>
          <Button variant="ghost" size="sm" onClick={onReset}>
            <RotateCcw className="size-3" />
            New agent
          </Button>
        </div>

        {/* Preview card */}
        <Card>
          <CardHeader className="border-b">
            <div className="flex items-center justify-between">
              <div>
                <CardTitle>github-issue-triager</CardTitle>
                <CardDescription>openclaw agent workspace</CardDescription>
              </div>
              <div className="flex gap-1">
                <Button
                  variant={activeTab === "config" ? "secondary" : "ghost"}
                  size="xs"
                  onClick={() => setActiveTab("config")}
                >
                  Config
                </Button>
                <Button
                  variant={activeTab === "files" ? "secondary" : "ghost"}
                  size="xs"
                  onClick={() => setActiveTab("files")}
                >
                  Files
                </Button>
              </div>
            </div>
          </CardHeader>
          <CardContent className="p-0">
            {activeTab === "config" ? (
              <div className="relative">
                <pre className="overflow-x-auto p-4 font-mono text-xs leading-relaxed text-muted-foreground">
                  {MOCK_CONFIG}
                </pre>
                <button
                  type="button"
                  onClick={handleCopy}
                  className="absolute top-3 right-3 border border-border bg-card p-1.5 text-muted-foreground transition-colors hover:text-foreground"
                >
                  {copied ? (
                    <Check className="size-3" />
                  ) : (
                    <Copy className="size-3" />
                  )}
                </button>
              </div>
            ) : (
              <div className="flex flex-col divide-y divide-border">
                {MOCK_FILES.map((file) => (
                  <div
                    key={file.name}
                    className="flex items-center gap-3 px-4 py-2 text-xs"
                  >
                    <file.icon className="size-3.5 text-muted-foreground/60" />
                    <span className="font-mono text-foreground">
                      {file.name}
                    </span>
                    {file.lines && (
                      <span className="text-muted-foreground/40">
                        {file.lines} lines
                      </span>
                    )}
                  </div>
                ))}
              </div>
            )}
          </CardContent>
        </Card>

        {/* Export actions */}
        <div className="grid gap-3 sm:grid-cols-3">
          <button
            type="button"
            className="group flex flex-col gap-2 border border-border p-4 text-left transition-colors hover:border-foreground/20"
          >
            <div className="flex items-center gap-2 text-sm font-medium text-foreground">
              <Copy className="size-3.5" />
              Copy config
            </div>
            <p className="text-xs text-muted-foreground">
              Copy the full workspace config to your clipboard
            </p>
          </button>

          <button
            type="button"
            className="group flex flex-col gap-2 border border-border p-4 text-left transition-colors hover:border-foreground/20"
          >
            <div className="flex items-center gap-2 text-sm font-medium text-foreground">
              <Download className="size-3.5" />
              Download ZIP
            </div>
            <p className="text-xs text-muted-foreground">
              Download the full agent workspace as a ZIP archive
            </p>
          </button>

          <button
            type="button"
            className="group flex flex-col gap-2 border border-border bg-foreground p-4 text-left text-background transition-colors hover:bg-foreground/90"
          >
            <div className="flex items-center gap-2 text-sm font-medium">
              <Rocket className="size-3.5" />
              Deploy to Railway
            </div>
            <p className="text-xs opacity-70">
              One-click deploy your agent to Railway infrastructure
            </p>
          </button>
        </div>

        {/* Clone command */}
        <div className="flex items-center gap-3 border border-border px-4 py-3">
          <GitBranch className="size-3.5 text-muted-foreground" />
          <code className="flex-1 font-mono text-xs text-muted-foreground">
            npx crabfold clone github-issue-triager ./my-agent
          </code>
          <button
            type="button"
            onClick={handleCopy}
            className="text-muted-foreground transition-colors hover:text-foreground"
          >
            <Copy className="size-3" />
          </button>
        </div>
      </div>
    </div>
  );
}
